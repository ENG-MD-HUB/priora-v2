/**
 * يرجع دالة ترجمة (t) لجلسة لغة معيّنة — نسخة من Be(lang).
 * الاستخدام: const t = createTranslator('ar'); t('dashboard') -> "لوحة التحكم"
 * يدعم استدعاء قيم على شكل دوال (مثل daysAgo) بتمرير معاملات إضافية.
 * يرجع نص اللغة الإنجليزية كـ fallback لو المفتاح غير موجود باللغة المطلوبة،
 * وأخيراً يرجع المفتاح نفسه لو غير موجود بأي مكان.
 */
export function createTranslator(lang) {
  return (key, ...args) => {
    const value = translations[lang]?.[key] ?? translations.en[key] ?? key;
    return typeof value === 'function' ? value(...args) : String(value);
  };
}
